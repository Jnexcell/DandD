/* Read-aloud card: front = scripted read-aloud; back (flip) = the FULL DM scene reference
   (expanded stat blocks + collapsible zones/checks/run-the-fight, Showdown A/B aware). */
function ReadAloud({ beat, flip, version, img, onImg }) {
  const b = BEATS[beat];
  const sceneRef = useRef(null);
  useEffect(() => {
    if (!flip) return;                       // only build the scene when the notes face shows
    const el = sceneRef.current; if (!el || !b) return;
    el.innerHTML = E.expandSB(b.body || "");
    collapsify(el);
    applyVer(el, version);
    const onClick = (ev) => {
      const t = ev.target; if (!t || !t.closest) return;
      if (t.closest("[data-secexpand]")) el.querySelectorAll("details.beat-sec").forEach((d) => d.open = true);
      else if (t.closest("[data-seccollapse]")) el.querySelectorAll("details.beat-sec").forEach((d) => d.open = false);
    };
    el.addEventListener("click", onClick);
    return () => el.removeEventListener("click", onClick);
  }, [beat, flip, version, b]);
  if (!b) return null;
  return (
    <div className={"ck-card" + (flip ? " flip" : "")}>
      <div className="ck-face ck-front">
        {img ? <img className="ck-ra-img" src={img} alt="" onError={(e) => { e.target.style.display = "none"; }} /> : null}
        {b.ra.length ? b.ra.map((r, i) => (
          <div key={i} className="ck-ra-block">
            <span className="ck-ra-eyebrow">📖 {r.label}</span>
            <div className="ck-ra-text" dangerouslySetInnerHTML={{ __html: r.html }} />
          </div>
        )) : <p className="ck-ra-text" style={{ opacity: .6 }}>No scripted read-aloud — narrate freely.</p>}
        <div className="ck-ra-imgrow">
          <span className="ck-ra-imglbl" title="local atmosphere image for this scene">🖼</span>
          <input className="ck-ra-imgin" placeholder="scene image path — e.g. img/scenes/scene1.jpg" value={img} onChange={(e) => onImg(e.target.value)} />
          {img ? <button className="ck-ra-imgx" title="clear" onClick={() => onImg("")}>✕</button> : null}
        </div>
      </div>
      <div className="ck-face ck-back">
        <span className="ck-ra-eyebrow secret">🔒 DM only — keep secret</span>
        <div className="ck-scene" ref={sceneRef} />
      </div>
    </div>
  );
}

function DiceTray({ active, sorted, onDamage, rollMode, setRollMode, log, onLog }) {
  const [mod, setMod] = useState(0);
  const [mode, setMode] = useState("normal");
  const [target, setTarget] = useState("");
  const [pending, setPending] = useState(null); // {dmg, label}
  const [tgtId, setTgtId] = useState("");
  const [d20v, setD20v] = useState("");  // manual natural d20
  const [dmgv, setDmgv] = useState("");  // manual damage total
  const manual = rollMode === "me";
  const pcs = sorted.filter((c) => c.side === "pc");
  const push = onLog; // shared combat log (lifted to Cockpit root; board drops write here too)
  const readD20 = () => { const n = parseInt(d20v, 10); return isNaN(n) ? null : clamp(n, 1, 20); };

  const rollCheck = () => {
    let die, dice;
    if (manual) { die = readD20(); if (die == null) return; dice = [die]; }
    else { const r = E.d20(0, mode); die = r.die; dice = r.dice; }
    const total = die + mod;
    const tgt = parseInt(target, 10);
    let verdict = "", kind = "roll";
    if (!isNaN(tgt)) { const hit = total >= tgt || die === 20; verdict = hit ? ` — HIT (vs ${tgt})` : ` — miss (vs ${tgt})`; kind = hit ? "hit" : "miss"; }
    if (die === 20) { verdict = " — NAT 20!"; kind = "hit"; }
    if (die === 1) { verdict = " — NAT 1"; kind = "miss"; }
    const dtxt = dice.length > 1 ? ` [${dice.join("/")}→${die}]` : ` [${die}]`;
    push({ kind, html: `d20${mod ? (mod > 0 ? "+" + mod : mod) : ""}${dtxt} = <b>${total}</b>${verdict}` });
    if (manual) setD20v("");
  };
  const rollFree = (s) => { const d = E.rollDie(s); push({ kind: "roll", html: `d${s} = <b>${d}</b>` }); };

  const rollAttack = (atk) => {
    let nat;
    if (manual) { nat = readD20(); if (nat == null) return; }
    else nat = E.rollDie(20);
    const crit = nat === 20;
    const toHitTotal = nat + (atk.tohit || 0);
    let dmgTotal = null;
    if (atk.dmgExpr) {
      if (manual && dmgv !== "") dmgTotal = parseInt(dmgv, 10);
      else { const d = E.rollExpr(atk.dmgExpr, { crit }); dmgTotal = d ? d.total : null; }
    }
    const tgtPc = pcs.find((p) => p.id === tgtId) || null;
    let vs = "", kind = "roll";
    if (tgtPc) { const hh = toHitTotal >= tgtPc.ac || crit; vs = ` vs ${tgtPc.name} AC ${tgtPc.ac} — ${hh ? "HIT" : "miss"}`; kind = hh ? "hit" : "miss"; }
    if (crit) kind = "hit";
    const dmgTxt = dmgTotal != null ? ` · <b>${dmgTotal}</b> ${atk.dmgType}${crit ? " (CRIT)" : ""}` : "";
    const saveTxt = atk.save ? ` · ${atk.save.ability} save DC ${atk.save.dc}` : "";
    push({ kind, html: `${atk.name}: d20+${atk.tohit} = <b>${toHitTotal}</b>${vs}${dmgTxt}${saveTxt}` });
    if (dmgTotal != null && tgtPc) setPending({ dmg: dmgTotal, label: `${dmgTotal} to ${tgtPc.name}`, id: tgtPc.id });
    else setPending(null);
    if (manual) { setD20v(""); setDmgv(""); }
  };

  const activeMon = active && active.side === "mon" && active.mkey ? MON[active.mkey] : null;

  return (
    <div className="ck-dice">
      <div className="ck-dice-row">
        <span className="ck-dice-lbl">Check / attack</span>
        <div className="ck-seg ck-rollmode">
          <button className={!manual ? "on" : ""} onClick={() => setRollMode("app")}>🎲 App</button>
          <button className={manual ? "on" : ""} onClick={() => setRollMode("me")}>✋ I roll</button>
        </div>
        <div className="ck-modbox">
          <button onClick={() => setMod((m) => m - 1)}>−</button>
          <span>mod {mod >= 0 ? "+" + mod : mod}</span>
          <button onClick={() => setMod((m) => m + 1)}>+</button>
        </div>
        {!manual && (
          <div className="ck-seg">
            {["dis", "normal", "adv"].map((mo) => (
              <button key={mo} className={mode === mo ? "on" : ""} onClick={() => setMode(mo)}>
                {mo === "dis" ? "Dis" : mo === "adv" ? "Adv" : "Norm"}
              </button>
            ))}
          </div>
        )}
        <input className="ck-tgt" inputMode="numeric" placeholder="vs AC/DC" value={target}
          onChange={(e) => setTarget(e.target.value.replace(/[^0-9]/g, ""))} />
        {manual ? (
          <div className="ck-manualroll">
            <input className="ck-d20in" inputMode="numeric" placeholder="d20" value={d20v}
              onChange={(e) => setD20v(e.target.value.replace(/[^0-9]/g, ""))}
              onKeyDown={(e) => { if (e.key === "Enter") rollCheck(); }} />
            <button className="ck-rolld20" onClick={rollCheck}>Log →</button>
          </div>
        ) : (
          <button className="ck-rolld20" onClick={rollCheck}>🎲 Roll d20</button>
        )}
      </div>

      <div className="ck-freedice">
        <span className="ck-dice-lbl">{manual ? "or auto-roll" : "quick"}</span>
        {[4, 6, 8, 10, 12, 20, 100].map((s) => <button key={s} onClick={() => rollFree(s)}>d{s}</button>)}
      </div>

      {activeMon && activeMon.attacks.length > 0 && (
        <div className="ck-attacks">
          <span className="ck-dice-lbl">{active.name}’s attacks{manual ? " — type your d20 (above), optional dmg, then tap" : ""}</span>
          <div className="ck-atk-row">
            {pcs.length > 0 && (
              <select className="ck-atk-tgt" value={tgtId} onChange={(e) => setTgtId(e.target.value)}>
                <option value="">target…</option>
                {pcs.map((p) => <option key={p.id} value={p.id}>{p.name} (AC {p.ac})</option>)}
              </select>
            )}
            {manual && (
              <input className="ck-d20in" inputMode="numeric" placeholder="dmg" value={dmgv}
                onChange={(e) => setDmgv(e.target.value.replace(/[^0-9]/g, ""))}
                title="your rolled damage — leave blank to auto-roll" />
            )}
            {activeMon.attacks.map((a, i) => (
              <button key={i} className="ck-atkbtn" onClick={() => rollAttack(a)}>
                {a.name} <small>+{a.tohit}{a.dmgExpr ? " · " + a.dmgExpr : ""}</small>
              </button>
            ))}
          </div>
          {pending && (
            <button className="ck-apply" onClick={() => { onDamage(pending.id, pending.dmg); setPending(null); }}>
              ⤵ Apply {pending.label}
            </button>
          )}
        </div>
      )}

      <div className="ck-log">
        {log.length === 0 && <span className="ck-log-empty">Rolls appear here.</span>}
        {log.map((e) => <div key={e.id} className={"ck-logitem " + e.kind} dangerouslySetInnerHTML={{ __html: e.html }} />)}
      </div>
    </div>
  );
}

