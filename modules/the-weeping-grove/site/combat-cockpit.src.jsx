/* what a chip consumes when it resolves: {kind:"slot",lvl} | {kind:"innate",key} | null (cantrip/at-will/weapon) */
function spendOf(ability) {
  if (!ability) return null;
  if (typeof ability.slotLevel === "number" && ability.slotLevel >= 1) return { kind: "slot", lvl: ability.slotLevel };
  const lv = ability.level;
  if (typeof lv === "string") {
    const sm = lv.match(/^(\d+)(?:st|nd|rd|th)$/i);
    if (sm) return { kind: "slot", lvl: +sm[1] };
    if (/\/\s*day/i.test(lv)) return { kind: "innate", key: (ability.note || ability.name || "").toLowerCase().trim() };
  }
  return null;
}
