/* {enemies:[{key,count}], addParty} (from the DM screen's ⚔ Combat) → a fresh, pre-initiative
   roster: the whole party + the scene's monsters, placed on the board, ready to roll. */
function buildLoadState(payload) {
  const combatants = [];
  if (payload.addParty) PARTY.forEach((p) => combatants.push(pcCombatant(p)));
  (payload.enemies || []).forEach(({ key, count }) => {
    if (!MON[key]) return;
    for (let i = 0; i < (count || 1); i++) combatants.push(monCombatant(key, i));
  });
  return { combatants: withPositions(combatants), round: 0, turnId: null, view: "board", selId: null };
}

