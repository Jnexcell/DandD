/* Normalize a PC attack / spell / monster action into a uniform "ability" descriptor.
   The resolve popover always lets the DM override the numbers, so heuristics are fine. */
function firstDice(text) {
  const m = String(text || "").match(/(\d+\s*d\s*\d+(?:\s*[+-]\s*\d+)?)/i);
  return m ? m[1].replace(/\s+/g, "") : null;
}
function parseDmgString(s) {
  if (!s) return { expr: null, type: "" };
  const str = String(s).replace(/\s+/g, " ").trim();
  const m = str.match(/(\d+\s*d\s*\d+(?:\s*[+-]\s*\d+)?)/i);
  const expr = m ? m[1].replace(/\s+/g, "") : null;
  const type = (m ? str.slice(m.index + m[0].length) : str).replace(/^[\s,;:·-]+/, "").trim();
  return { expr, type };
}
/* extract an imposed condition (and whether it lands on a hit or a failed save) from ability text */
const COND_WORDS = { prone: "Prone", restrained: "Restrained", grappled: "Grappled", stunned: "Stunned", paralyzed: "Paralyzed", frightened: "Frightened", poisoned: "Poisoned", blinded: "Blinded" };
/* spells whose NAME implies a tracked condition (monster innate/prepared spells carry no effect text) */
const SPELL_COND = { "hold person": "Paralyzed", "hold monster": "Paralyzed", "sleep": "Stunned", "command": "Prone", "color spray": "Blinded", "blindness": "Blinded", "entangle": "Restrained", "web": "Restrained", "ensnaring strike": "Restrained", "fear": "Frightened", "cause fear": "Frightened", "ray of sickness": "Poisoned", "contagion": "Poisoned" };
function parseCond(text) {
  const t = String(text || "");
  for (const w in COND_WORDS) {
    if (new RegExp("\\b" + w + "\\b", "i").test(t)) {
      return { cond: COND_WORDS[w], on: /sav(e|ing)|\bDC\b/i.test(t) ? "fail" : "hit" };
    }
  }
  const low = t.toLowerCase();
  for (const sp in SPELL_COND) { if (low.indexOf(sp) >= 0) return { cond: SPELL_COND[sp], on: "fail" }; }
  return null;
}
/* conditions/actions → advantage/disadvantage on an attack, with a human reason (DM can override).
   Target-side conds grant adv (restrained/stunned/blinded) or depend on melee/ranged (prone);
   attacker-side conds (prone/restrained/frightened/poisoned/blinded) + target Dodge give dis. */
function advInfo(ability, target, source) {
  const reasons = []; let adv = false, dis = false;
  if (!target) return { mode: "normal", reasons };
  const ranged = /ranged|range \d|crossbow|\bbow\b|longbow|shortbow|thrown|\bdart\b|\bsling\b/i.test(ability.note || "");
  const tc = target.conds || [], ta = target.actions || {};
  if (tc.includes("Prone")) { if (ranged) { dis = true; reasons.push("target Prone → disadvantage (ranged)"); } else { adv = true; reasons.push("target Prone → advantage (melee)"); } }
  if (tc.includes("Restrained")) { adv = true; reasons.push("target Restrained → advantage"); }
  if (tc.includes("Stunned")) { adv = true; reasons.push("target Stunned → advantage"); }
  if (tc.includes("Paralyzed")) { adv = true; reasons.push("target Paralyzed → advantage"); }
  if (tc.includes("Blinded")) { adv = true; reasons.push("target Blinded → advantage"); }
  if (ta.dodge) { dis = true; reasons.push("target Dodging → disadvantage"); }
  if (source) {
    const sc = source.conds || [], sa = source.actions || {};
    ["Prone", "Restrained", "Frightened", "Poisoned", "Blinded"].forEach((c) => {
      if (sc.includes(c)) { dis = true; reasons.push(source.name + " " + c + " → disadvantage"); }
    });
    if (sa.dodge) { /* dodging doesn't impair your own attacks */ }
  }
  const mode = (adv && dis) ? "normal" : adv ? "adv" : dis ? "dis" : "normal";
  if (adv && dis) reasons.push("advantage + disadvantage cancel → normal");
  return { mode, reasons };
}
function pcAttackAbility(a) {
  const d = parseDmgString(a.dmg);
  const cc = parseCond(a.note || "");
  return { name: a.name, kind: "attack", tohit: E.toNum(a.bonus), dmgExpr: d.expr, dmgType: d.type, note: a.note || a.range || "", cond: cc ? cc.cond : null, condOn: cc ? cc.on : null, src: a };
}
function pcSpellAbility(sp, s) {
  const roll = s.roll || "", eff = s.effect || "", dice = firstDice(eff);
  let kind = "utility", tohit = null, save = null, dmgExpr = dice, dmgType = "", healExpr = null;
  if (/spell attack/i.test(roll)) { kind = "attack"; tohit = E.toNum(sp.atk); dmgType = "spell"; }
  else if (/hit points|\brestore\b|\bheal/i.test(eff) || /cure|heal/i.test(s.name)) { kind = "heal"; healExpr = dice; dmgExpr = null; }
  else if (/sav(e|ing)/i.test(roll) || /\bDC\b/.test(eff + " " + roll)) {
    kind = "save";
    const ab = (roll + " " + eff).match(/(Str|Dex|Con|Int|Wis|Cha)\w*\s+sav/i);
    save = { dc: sp.dc, ability: ab ? ab[1] : "" };
  }
  const cc = parseCond(eff);
  return { name: s.name, kind, tohit, dmgExpr, dmgType, healExpr, save, note: eff, cond: cc ? cc.cond : null, condOn: cc ? cc.on : null,
    level: s.lvl === "cantrip" ? "cantrip" : "L" + s.lvl, slotLevel: (typeof s.lvl === "number" && s.lvl >= 1) ? s.lvl : null, src: s };
}
function monActionAbility(act) {
  const p = E.parseAttack(act.n, act.t);
  const kind = p.tohit != null ? "attack" : (p.save ? "save" : "utility");
  const cc = parseCond(act.t);
  return { name: act.n, kind, tohit: p.tohit || 0, dmgExpr: p.dmgExpr, dmgType: p.dmgType, save: p.save, note: act.t, cond: cc ? cc.cond : null, condOn: cc ? cc.on : null, src: act };
}
/* monster spellcasting → draggable spell chips. Derive the spell list, save DC and attack
   bonus from the statblock's Spellcasting / Innate Spellcasting trait text (single source of
   truth — no hardcoded per-monster lists). Kind via the same heuristics as PCs; parseCond
   auto-applies condition spells (hold person → Paralyzed, etc.). DM overrides in the popover. */
const SP_HEAL = /\b(cure wounds|healing word|mass cure|heal\b|prayer of healing)/i;
const SP_ATK = /\b(inflict wounds|guiding bolt|fire bolt|firebolt|ray of frost|chill touch|shocking grasp|eldritch blast|witch bolt|scorching ray|thorn whip|produce flame|spiritual weapon|acid splash|poison spray|frostbite)\b/i;
const SP_UTIL = /\b(light|thaumaturgy|dancing lights|minor illusion|major image|silent image|mage armor|shield of faith|bless|aid|invisibility|disguise self|silence|darkness|fog cloud|detect magic|mending|message|prestidigitation|spare the dying|guidance|resistance|pass without trace|misty step)\b/i;
const titleCase = (s) => String(s).replace(/\b\w/g, (m) => m.toUpperCase());
function monSpellAbilities(sb) {
  const out = [];
  (sb.traits || []).forEach((tr) => {
    if (!/spellcasting/i.test(tr.n || "")) return;
    const raw = String(tr.t || "").replace(/<[^>]+>/g, " ").replace(/\s+/g, " ").trim();
    const dc = (raw.match(/save\s*DC\s*(\d+)/i) || [])[1];
    const atk = (raw.match(/([+\-−–]\s*\d+)\s*to hit/i) || [])[1];
    const cab = (raw.match(/\b(Str|Dex|Con|Int|Wis|Cha)\b/i) || [])[1] || "";
    const re = /(Cantrips?|At will|\d+\s*\/\s*day(?:\s+each)?|\d+(?:st|nd|rd|th))\s*(?:\([^)]*\))?\s*:\s*([^;.]+)/gi;
    let m;
    while ((m = re.exec(raw))) {
      const lvl = /cantrip/i.test(m[1]) ? "cantrip" : m[1].toLowerCase().replace(/\s+/g, " ").trim();
      m[2].split(",").forEach((nm) => {
        const name = nm.trim().replace(/[.;]+$/, "");
        if (!name) return;
        const low = name.toLowerCase();
        const cc = parseCond(name);
        let kind = "utility", tohit = null, save = null, dmgType = "", healExpr = null;
        if (SP_HEAL.test(low)) kind = "heal";
        else if (SP_ATK.test(low)) { kind = "attack"; tohit = E.toNum(atk); dmgType = "spell"; }
        else if (SP_UTIL.test(low) && !cc) kind = "utility";
        else if (cc || dc) { kind = "save"; save = { dc: dc ? +dc : null, ability: cab }; }
        out.push({ name: titleCase(name), kind, tohit, dmgExpr: null, dmgType, healExpr, save, note: name, level: lvl, cond: cc ? cc.cond : null, condOn: cc ? cc.on : "fail", src: null });
      });
    }
  });
  return out;
}
function abilitiesFor(c) {
  if (c.side === "pc") {
    const pc = PARTY_BY_SLUG[c.slug]; if (!pc) return [];
    const out = (pc.attacks || []).map(pcAttackAbility);
    if (pc.spell) [].concat(pc.spell.cantrips || []).concat(pc.spell.known || pc.spell.prepared || []).forEach((s) => out.push(pcSpellAbility(pc.spell, s)));
    return out;
  }
  const sb = SB[c.mkey]; if (!sb) return [];
  const acts = (sb.actions || []).map(monActionAbility).filter((a) => a.kind === "attack" || a.kind === "save");
  return acts.concat(monSpellAbilities(sb));
}
function tokLabel(c) {
  if (c.side === "pc") return (c.name || "?").trim().charAt(0).toUpperCase();
  const m = (c.name || "").match(/^(.*?)\s*(\d+)\s*$/);
  return m ? (m[1].charAt(0).toUpperCase() + m[2]) : (c.name || "?").slice(0, 3);
}
/* readable name shown ABOVE the token (PCs: first name; monsters: the numbered name e.g. "Wolf 3") */
const tokName = (c) => (c.side === "pc" ? (c.name || "").trim().split(/\s+/)[0] : (c.name || "").trim()) || "?";

/* level/slot label for chips + tooltip ("L1" / "cantrip" / "1st" / "At will" / "3/day each") */
function abilLevelLabel(ability) {
  const lv = ability.level;
  if (!lv) return "";
  const s = String(lv);
  return s.charAt(0).toUpperCase() + s.slice(1);
}
/* Click (not drag) on an ability chip → a read-only detail tooltip. PC spells/attacks and monster
   actions carry full text (ability.src = the raw spell/attack/action); monster spells only have a
   name in the data, so we show the derived fields + a note. (Richer monster-spell text = add a
   field to the data file, not hardcode here.) */
function AbilityTip({ ability, posStyle, onClose }) {
  const s = ability.src || null;
  const rows = [];
  const lvl = abilLevelLabel(ability);
  if (lvl) rows.push(["Level", lvl]);
  if (s && s.castTime) rows.push(["Cast", s.castTime]);
  else if (s && s.use) rows.push(["Action", s.use]);
  if (s && s.range) rows.push(["Range", s.range]);
  if (ability.kind === "attack") rows.push(["To hit", "+" + (ability.tohit || 0)]);
  if (ability.dmgExpr) rows.push(["Damage", ability.dmgExpr + (ability.dmgType ? " " + ability.dmgType : "")]);
  if (ability.save && ability.save.dc != null) rows.push(["Save", "DC " + ability.save.dc + (ability.save.ability ? " " + ability.save.ability : "")]);
  if (ability.healExpr) rows.push(["Heal", ability.healExpr]);
  if (s && s.concentration) rows.push(["Concentration", "yes"]);
  if (s && s.duration) rows.push(["Duration", s.duration]);
  if (ability.cond) rows.push(["Condition", ability.cond + " (on " + (ability.condOn || "hit") + ")"]);
  let textHtml = null, textPlain = null;
  if (s && s.effect) textPlain = s.effect;          // PC spell effect
  else if (s && s.note) textPlain = s.note;          // PC attack note
  else if (s && s.t) textHtml = s.t;                 // monster action (HTML)
  else textPlain = "No description in the stat block — narrate from the spell's name + level.";
  if (s && s.when) textPlain = (textPlain ? textPlain + "  " : "") + "▸ " + s.when;
  return (
    <div className="ck-tip" style={posStyle}>
      <div className="ck-tip-head"><b>{ability.name}</b><button className="ck-tip-x" onClick={onClose}>✕</button></div>
      {rows.length > 0 && <div className="ck-tip-rows">{rows.map((r, i) => (
        <div key={i} className="ck-tip-row"><span className="ck-tip-k">{r[0]}</span><span className="ck-tip-v">{r[1]}</span></div>
      ))}</div>}
      {textHtml ? <div className="ck-tip-text" dangerouslySetInnerHTML={{ __html: textHtml }} />
        : <div className="ck-tip-text">{textPlain}</div>}
    </div>
  );
}

/* Popover shown when an ability chip is dropped on a target. Honors the roll mode
   (app = auto-roll via TWGE; me = the DM types the numbers). Applies via damage/heal + logs. */
function ResolvePopover({ ability, target, source, rollMode, onDamage, onHeal, onPatch, onLog, onClose, posStyle }) {
  const manual = rollMode === "me";
  const adv = advInfo(ability, target, source);
  const [d20v, setD20v] = useState("");
  const [dmgv, setDmgv] = useState("");
  const [res, setRes] = useState(null);
  const [mode, setMode] = useState(adv.mode);   // pre-picked from conditions; DM can override
  const roll = () => {
    if (ability.kind === "heal") { const r = E.rollExpr(ability.healExpr || "0"); setRes({ heal: r ? r.total : 0 }); return; }
    const r = E.d20(ability.tohit || 0, mode), nat = r.die, crit = r.nat20, total = r.total;
    const hit = ability.kind === "save" ? null : (total >= target.ac || crit);
    let dmg = null; if (ability.dmgExpr) { const d = E.rollExpr(ability.dmgExpr, { crit }); dmg = d ? d.total : null; }
    setRes({ nat, total, hit, crit, dmg, dice: r.dice });
  };
  useEffect(() => { if (!manual) roll(); }, [mode]);
  const applyCond = () => { if (ability.cond && onPatch) onPatch(target.id, (cc) => ({ conds: cc.conds.includes(ability.cond) ? cc.conds : [...cc.conds, ability.cond] })); };
  const condNote = ability.cond ? ` · ${condIcon(ability.cond)} ${ability.cond}` : "";
  const saveHint = (() => {
    if (!ability.save) return "";
    const ab = (ability.save.ability || "").slice(0, 3).toLowerCase(), tc = target.conds || [];
    if (tc.includes("Paralyzed") && (ab === "str" || ab === "dex")) return "target Paralyzed → auto-fails Str/Dex saves";
    if (tc.includes("Stunned") && (ab === "str" || ab === "dex")) return "target Stunned → auto-fails Str/Dex saves";
    if (tc.includes("Restrained") && ab === "dex") return "target Restrained → disadvantage on Dex saves";
    return "";
  })();
  const dmgVal = () => manual ? (parseInt(dmgv, 10) || 0) : (res ? res.dmg || 0 : 0);
  const healVal = () => manual ? (parseInt(dmgv, 10) || 0) : (res ? res.heal || 0 : 0);
  const crit = () => manual ? (parseInt(d20v, 10) === 20) : (res ? res.crit : false);
  const total = () => manual ? ((parseInt(d20v, 10) || 0) + (ability.tohit || 0)) : (res ? res.total : 0);
  const hit = () => manual ? (total() >= target.ac || crit()) : (res ? res.hit : false);

  /* resolving an ability spends the source's economy: mark its Action used, and decrement the
     matching spell slot / innate use for a leveled spell (cantrips/at-will/weapons cost no slot).
     A slot is spent whether the attack hits or the save succeeds. DM can re-toggle in the panel. */
  const spend = () => {
    if (!source || !onPatch) return;
    const sp = spendOf(ability);
    onPatch(source.id, (cc) => {
      const patch = { econ: { action: true, bonus: !!(cc.econ && cc.econ.bonus), reaction: !!(cc.econ && cc.econ.reaction) } };
      if (sp && sp.kind === "slot") { const cur = (cc.slots && cc.slots[sp.lvl]) || 0; patch.slots = { ...(cc.slots || {}), [sp.lvl]: Math.max(0, cur - 1) }; }
      else if (sp && sp.kind === "innate") { const cur = (cc.innate && cc.innate[sp.key]) || 0; patch.innate = { ...(cc.innate || {}), [sp.key]: Math.max(0, cur - 1) }; }
      return patch;
    });
  };

  const applyAttack = () => {
    const isHit = hit(), dmg = dmgVal();
    if (isHit && dmg) onDamage(target.id, dmg);
    if (isHit) applyCond();
    onLog({ kind: isHit ? "hit" : "miss", html: `${ability.name} → <b>${target.name}</b> (AC ${target.ac}): d20+${ability.tohit || 0} = <b>${total()}</b> — ${isHit ? "HIT" : "miss"}${isHit && dmg ? ` · <b>${dmg}</b> ${ability.dmgType || ""}${crit() ? " (CRIT)" : ""}` : ""}${isHit ? condNote : ""}` });
    spend();
    onClose();
  };
  const applySave = (out) => {
    const dmg = dmgVal(), applied = out === "fail" ? dmg : out === "half" ? Math.floor(dmg / 2) : 0;
    if (applied) onDamage(target.id, applied);
    if (out === "fail") applyCond();
    onLog({ kind: out === "fail" ? "hit" : "roll", html: `${ability.name} → <b>${target.name}</b>: DC ${ability.save ? ability.save.dc : "?"} ${ability.save ? ability.save.ability : ""} save — ${out === "fail" ? "FAILED" : out === "half" ? "saved ½" : "saved"}${applied ? ` · <b>${applied}</b> ${ability.dmgType || ""}` : ""}${out === "fail" ? condNote : ""}` });
    spend();
    onClose();
  };
  const applyHeal = () => { const amt = healVal(); if (amt) onHeal(target.id, amt); onLog({ kind: "roll", html: `${ability.name} → <b>${target.name}</b>: healed <b>${amt}</b>` }); spend(); onClose(); };
  const applyUtil = () => { onLog({ kind: "roll", html: `${ability.name} → <b>${target.name}</b>` }); spend(); onClose(); };

  const head = (
    <div className="ck-pop-head">
      <b>{ability.name}</b><span className="ck-pop-arrow">{"→ " + target.name}</span>
      <button className="ck-pop-x" onClick={onClose}>✕</button>
    </div>
  );
  let body;
  if (ability.kind === "attack") {
    body = (
      <div className="ck-pop-body">
        <div className="ck-pop-adv">
          <div className="ck-seg ck-pop-mode">
            {["dis", "normal", "adv"].map((mo) => (
              <button key={mo} className={mode === mo ? "on" : ""} onClick={() => setMode(mo)}>{mo === "dis" ? "Dis" : mo === "adv" ? "Adv" : "Norm"}</button>
            ))}
          </div>
          {adv.reasons.length > 0 && <span className="ck-pop-why">{adv.reasons.join(" · ")}</span>}
        </div>
        {manual ? (
          <div className="ck-pop-row">
            <label>d20</label>
            <input className="ck-d20in sm" inputMode="numeric" autoFocus value={d20v} onChange={(e) => setD20v(e.target.value.replace(/[^0-9]/g, ""))} />
            <span className="ck-pop-eq">{"+" + (ability.tohit || 0) + " = " + total()}</span>
            <span className={hit() ? "ck-pop-hit" : "ck-pop-miss"}>{"vs AC " + target.ac + " · " + (hit() ? "HIT" : "miss")}</span>
          </div>
        ) : (
          <div className="ck-pop-row">
            <span className={res && res.hit ? "ck-pop-hit" : "ck-pop-miss"}>{"d20 " + (res ? res.nat : "?") + (res && res.dice && res.dice.length > 1 ? " [" + res.dice.join("/") + "]" : "") + "+" + (ability.tohit || 0) + " = " + total() + " vs AC " + target.ac + " · " + (res && res.hit ? "HIT" : "miss") + (res && res.crit ? " CRIT" : "")}</span>
            <button className="ck-pop-btn ghost" onClick={roll}>🎲</button>
          </div>
        )}
        <div className="ck-pop-row">
          <label>dmg</label>
          {manual
            ? <input className="ck-d20in" inputMode="numeric" placeholder={ability.dmgExpr || "#"} value={dmgv} onChange={(e) => setDmgv(e.target.value.replace(/[^0-9]/g, ""))} />
            : <span className="ck-pop-dmg">{(res && res.dmg != null ? res.dmg : "—") + " " + (ability.dmgType || "") + (ability.dmgExpr ? " (" + ability.dmgExpr + ")" : "")}</span>}
        </div>
        {ability.cond && <div className="ck-pop-why cond">{condIcon(ability.cond) + " applies " + ability.cond + " on hit"}</div>}
        <button className="ck-pop-btn apply" onClick={applyAttack}>{hit() ? ("⚔ Apply " + dmgVal() + " dmg") : "Log miss"}</button>
      </div>
    );
  } else if (ability.kind === "save") {
    body = (
      <div className="ck-pop-body">
        <div className="ck-pop-save">{"DC " + (ability.save ? ability.save.dc : "?") + " " + (ability.save ? ability.save.ability : "") + " save — player rolls"}</div>
        {saveHint && <div className="ck-pop-why">{saveHint}</div>}
        {ability.cond && <div className="ck-pop-why cond">{condIcon(ability.cond) + " applies " + ability.cond + " on failed save"}</div>}
        <div className="ck-pop-row">
          <label>dmg</label>
          {manual
            ? <input className="ck-d20in" inputMode="numeric" placeholder={ability.dmgExpr || "#"} value={dmgv} onChange={(e) => setDmgv(e.target.value.replace(/[^0-9]/g, ""))} />
            : <span className="ck-pop-dmg">{(res && res.dmg != null ? res.dmg : "—") + " " + (ability.dmgType || "")}</span>}
          {!manual && ability.dmgExpr && <button className="ck-pop-btn ghost" onClick={roll}>🎲</button>}
        </div>
        <div className="ck-pop-saverow">
          <button className="ck-pop-btn fail" onClick={() => applySave("fail")}>Failed — full</button>
          <button className="ck-pop-btn" onClick={() => applySave("half")}>Saved — ½</button>
          <button className="ck-pop-btn ghost" onClick={() => applySave("none")}>None</button>
        </div>
      </div>
    );
  } else if (ability.kind === "heal") {
    body = (
      <div className="ck-pop-body">
        <div className="ck-pop-row">
          <label>heal</label>
          {manual
            ? <input className="ck-d20in" inputMode="numeric" autoFocus placeholder={ability.healExpr || "#"} value={dmgv} onChange={(e) => setDmgv(e.target.value.replace(/[^0-9]/g, ""))} />
            : <span className="ck-pop-dmg">{(res ? res.heal : "—") + (ability.healExpr ? " (" + ability.healExpr + ")" : "")}</span>}
          {!manual && <button className="ck-pop-btn ghost" onClick={roll}>🎲</button>}
        </div>
        <button className="ck-pop-btn apply heal" onClick={applyHeal}>{"✚ Apply " + healVal() + " heal"}</button>
      </div>
    );
  } else {
    body = (
      <div className="ck-pop-body">
        <div className="ck-pop-note">{ability.note || "No roll — narrate it."}</div>
        <button className="ck-pop-btn apply" onClick={applyUtil}>{"Log on " + target.name}</button>
      </div>
    );
  }
  return <div className="ck-pop" style={posStyle}>{head}{body}</div>;
}

function BoardView({ sorted, active, st, up, onDamage, onHeal, onPatch, onRemove, onLog, version, onSetTurn }) {
  const boardRef = useRef(null);
  const worldRef = useRef(null);   // the scaled+translated "world" layer (grid + tokens + rings + ruler)
  const dragRef = useRef(null);
  const chipRef = useRef(null);
  const panRef = useRef(null);
  const [drag, setDrag] = useState(null);
  const [chip, setChip] = useState(null);
  const [abilTip, setAbilTip] = useState(null);   // {ability,x,y} — chip clicked (not dragged) → detail tooltip
  const [resolve, setResolve] = useState(null);
  const [rulerMode, setRulerMode] = useState(false);
  const [ruler, setRuler] = useState(null);   // {x1,y1,x2,y2,ft} in *untransformed world px* (scale with zoom)
  const [panning, setPanning] = useState(false);
  const [vp, setVp] = useState({ zoom: 1, x: 0, y: 0 });   // viewport: scale + pan (px). Ephemeral — not persisted.
  const rulerRef = useRef(null);

  /* world point (untransformed px) under a client coordinate, honouring the current zoom/pan */
  const worldPt = (cx, cy) => {
    const rect = worldRef.current.getBoundingClientRect();   // post-transform box → already includes zoom+pan
    return { x: (cx - rect.left) / vp.zoom, y: (cy - rect.top) / vp.zoom };
  };

  /* scroll-wheel zoom toward the cursor (native non-passive so preventDefault stops page scroll) */
  const onWheel = useCallback((e) => {
    e.preventDefault();
    const el = boardRef.current; if (!el) return;
    const rect = el.getBoundingClientRect();
    const mx = e.clientX - rect.left, my = e.clientY - rect.top;   // cursor in surface-local px
    const factor = e.deltaY < 0 ? 1.12 : 1 / 1.12;
    setVp((v) => {
      const nz = clamp(+(v.zoom * factor).toFixed(3), ZMIN, ZMAX);
      const wx = (mx - v.x) / v.zoom, wy = (my - v.y) / v.zoom;   // world point under cursor (keep it fixed)
      return { zoom: nz, x: mx - wx * nz, y: my - wy * nz };
    });
  }, [vp.zoom]);
  useEffect(() => {
    const el = boardRef.current; if (!el) return;
    el.addEventListener("wheel", onWheel, { passive: false });
    return () => el.removeEventListener("wheel", onWheel);
  }, [onWheel]);
  const zoomBy = (factor) => setVp((v) => {
    const el = boardRef.current, mx = el ? el.clientWidth / 2 : 0, my = el ? el.clientHeight / 2 : 0;
    const nz = clamp(+(v.zoom * factor).toFixed(3), ZMIN, ZMAX);
    const wx = (mx - v.x) / v.zoom, wy = (my - v.y) / v.zoom;
    return { zoom: nz, x: mx - wx * nz, y: my - wy * nz };
  });
  const resetView = () => setVp({ zoom: 1, x: 0, y: 0 });

  /* surface pointer: distance ruler (ruler mode) OR drag-to-pan (otherwise) on empty board.
     Tokens/controls handle their own pointers, so skip them here. */
  const onSurface = (e) => e.target.closest && e.target.closest("[data-tokenid], button, input, .ck-pop, .ck-detail");
  const surfDown = (e) => {
    if (e.button || onSurface(e)) return;
    setAbilTip(null);                              // tapping the board dismisses an open chip tooltip
    if (rulerMode) {
      const p = worldPt(e.clientX, e.clientY);
      rulerRef.current = { x1: p.x, y1: p.y };
      setRuler({ x1: p.x, y1: p.y, x2: p.x, y2: p.y, ft: 0 });
    } else {
      panRef.current = { sx: e.clientX, sy: e.clientY, px: vp.x, py: vp.y };
      setPanning(true);
    }
    try { e.currentTarget.setPointerCapture(e.pointerId); } catch (z) {}
  };
  const surfMove = (e) => {
    if (rulerRef.current) {
      const d = rulerRef.current, p = worldPt(e.clientX, e.clientY);
      setRuler({ x1: d.x1, y1: d.y1, x2: p.x, y2: p.y, ft: ftBetween(d.x1, d.y1, p.x, p.y) });
    } else if (panRef.current) {
      const d = panRef.current;
      setVp((v) => ({ ...v, x: d.px + (e.clientX - d.sx), y: d.py + (e.clientY - d.sy) }));
    }
  };
  const surfUp = (e) => { rulerRef.current = null; panRef.current = null; setPanning(false); try { e.currentTarget.releasePointerCapture(e.pointerId); } catch (z) {} };

  const startToken = (e, c) => {
    if (e.button) return;
    setAbilTip(null);
    const rect = worldRef.current.getBoundingClientRect();   // post-transform → fractions stay correct under zoom/pan
    dragRef.current = { id: c.id, sx: e.clientX, sy: e.clientY, offX: e.clientX - (rect.left + c.x * rect.width), offY: e.clientY - (rect.top + c.y * rect.height), moved: false, lx: c.x, ly: c.y };
    try { e.currentTarget.setPointerCapture(e.pointerId); } catch (x) {}
  };
  const moveToken = (e) => {
    const d = dragRef.current; if (!d) return;
    if (!d.moved && Math.hypot(e.clientX - d.sx, e.clientY - d.sy) < 5) return;
    d.moved = true;
    const rect = worldRef.current.getBoundingClientRect();
    d.lx = clamp((e.clientX - d.offX - rect.left) / rect.width, 0.03, 0.97);
    d.ly = clamp((e.clientY - d.offY - rect.top) / rect.height, 0.05, 0.95);
    setDrag({ id: d.id, x: d.lx, y: d.ly });
  };
  const endToken = (e, c) => {
    const d = dragRef.current; dragRef.current = null;
    try { e.currentTarget.releasePointerCapture(e.pointerId); } catch (x) {}
    if (d && d.moved) onPatch(c.id, { x: d.lx, y: d.ly });
    else { setAbilTip(null); up({ selId: c.id }); if (onSetTurn && st.round > 0) onSetTurn(c.id); }  // tap = select + (mid-fight) make it this combatant's turn
    setDrag(null);
  };

  const tokenAt = (x, y) => { const el = document.elementFromPoint(x, y); const t = el && el.closest ? el.closest("[data-tokenid]") : null; return t ? t.getAttribute("data-tokenid") : null; };
  const startChip = (e, ability) => {
    if (e.button) return;
    chipRef.current = { ability, sx: e.clientX, sy: e.clientY, moved: false };  // track movement so we can tell a click (tooltip) from a drag (apply)
    try { e.currentTarget.setPointerCapture(e.pointerId); } catch (x) {}
    e.preventDefault();
  };
  const moveChip = (e) => {
    const cr = chipRef.current; if (!cr) return;
    if (!cr.moved && Math.hypot(e.clientX - cr.sx, e.clientY - cr.sy) < 5) return;   // same <5px threshold as token drag
    cr.moved = true;
    setChip({ ability: cr.ability, x: e.clientX, y: e.clientY, overId: tokenAt(e.clientX, e.clientY) });
  };
  const endChip = (e) => {
    const cr = chipRef.current; chipRef.current = null;
    try { e.currentTarget.releasePointerCapture(e.pointerId); } catch (x) {}
    if (!cr) return;
    if (!cr.moved) {                                   // click (no drag) → show the ability detail tooltip
      setChip(null);
      if (cr.ability.kind === "condition") return;     // conditions have no detail to show
      setAbilTip({ ability: cr.ability, x: e.clientX, y: e.clientY });
      return;
    }
    const targetId = tokenAt(e.clientX, e.clientY);
    setChip(null);
    if (!targetId) return;
    const ability = cr.ability;
    if (ability.kind === "condition") {
      onPatch(targetId, (cc) => ({ conds: cc.conds.includes(ability.cond) ? cc.conds : [...cc.conds, ability.cond] }));
      const tg = sorted.find((c) => c.id === targetId);
      onLog({ kind: "roll", html: `${condIcon(ability.cond)} ${ability.cond} → <b>${tg ? tg.name : "?"}</b>` });
    } else setResolve({ ability, targetId, sourceId: st.selId });
  };

  const sel = sorted.find((c) => c.id === st.selId) || null;
  const resTarget = resolve ? sorted.find((c) => c.id === resolve.targetId) : null;
  let posStyle = null;
  if (resTarget && worldRef.current) {
    const rect = worldRef.current.getBoundingClientRect();   // post-transform → token's real screen box
    const vw = window.innerWidth, vh = window.innerHeight;
    posStyle = { left: clamp(rect.left + resTarget.x * rect.width, 140, vw - 140) + "px", top: clamp(rect.top + resTarget.y * rect.height + 26, 70, vh - 200) + "px" };
  }
  let tipStyle = null;
  if (abilTip) { const vw = window.innerWidth, vh = window.innerHeight; tipStyle = { left: clamp(abilTip.x, 150, vw - 150) + "px", top: clamp(abilTip.y + 14, 60, vh - 220) + "px" }; }
  const worldStyle = { transform: `translate(${vp.x}px,${vp.y}px) scale(${vp.zoom})`, transformOrigin: "0 0" };

  return (
    <div className={"ck-board" + (sel ? " has-detail" : "")}>
      <div className={"ck-surface" + (panning ? " panning" : "") + (rulerMode ? " ruling" : "")} ref={boardRef}
        onPointerDown={surfDown} onPointerMove={surfMove} onPointerUp={surfUp}>
        <button className={"ck-ruler-btn" + (rulerMode ? " on" : "")} title="distance ruler (drag on the board)"
          onClick={() => { setRulerMode((m) => !m); setRuler(null); }}>📏</button>
        <div className="ck-zoom">
          <button className="ck-zoom-out" title="zoom out" onClick={() => zoomBy(1 / 1.2)}>−</button>
          <button className="ck-zoom-reset" title="reset view" onClick={resetView}>{Math.round(vp.zoom * 100) + "%"}</button>
          <button className="ck-zoom-in" title="zoom in" onClick={() => zoomBy(1.2)}>+</button>
        </div>
        {sorted.length === 0 && <p className="ck-board-empty">Add fighters from the roster, then drag them into position.</p>}
        <div className="ck-world" ref={worldRef} style={worldStyle}>
          {ruler && <svg className="ck-ruler-svg"><line x1={ruler.x1} y1={ruler.y1} x2={ruler.x2} y2={ruler.y2} /></svg>}
          {sorted.map((c) => {
            const dragging = drag && drag.id === c.id;
            const x = dragging ? drag.x : c.x, y = dragging ? drag.y : c.y;
            const down = c.hp <= 0;
            const pct = clamp((c.hp / c.maxhp) * 100, 0, 100);
            const band = down || pct <= 25 ? "crit" : pct <= 50 ? "low" : "ok";
            const bloodied = !down && pct <= 50;
            const pc = c.side === "pc" ? PARTY_BY_SLUG[c.slug] : null;
            const sil = tokSil(c);
            const acts = c.actions ? Object.keys(c.actions).filter((k) => c.actions[k]) : [];
            const ringSel = !down && ((c.id === st.selId) || (active && c.id === active.id));
            const reachD = (speedOf(c) / 5) * CELL_PX * 2;
            const cls = "ck-tok " + c.side + (c.id === st.selId ? " sel" : "") + (active && c.id === active.id ? " active" : "") + (chip && chip.overId === c.id ? " over" : "") + (bloodied ? " bloodied" : "") + (down ? " down" : "") + (dragging ? " dragging" : "");
            return (
              <div key={c.id} data-tokenid={c.id} className={cls} title={c.name} style={{ left: x * 100 + "%", top: y * 100 + "%" }}
                onPointerDown={(e) => startToken(e, c)} onPointerMove={moveToken} onPointerUp={(e) => endToken(e, c)}>
                <span className="ck-tok-name">{tokName(c)}</span>
                {ringSel && c.actions && c.actions.dash && <div className="ck-ring dash" style={{ width: reachD * 2, height: reachD * 2 }} />}
                {ringSel && <div className="ck-ring reach" style={{ width: reachD, height: reachD }} />}
                <span className="ck-tok-label">
                  {sil && <span className="ck-tok-sil" dangerouslySetInnerHTML={{ __html: sil }} />}
                  {pc && pc.portrait
                    ? <img className="ck-tok-img" src={pc.portrait} alt="" draggable={false} onError={(e) => { e.target.style.display = "none"; }} />
                    : <span className="ck-tok-txt">{tokLabel(c)}</span>}
                </span>
                <div className="ck-tok-hp"><i className={band} style={{ width: pct + "%" }} /></div>
                <span className="ck-tok-hpn">{c.hp + "/" + c.maxhp}</span>
                {c.conds.length > 0 && <div className="ck-tok-conds">{c.conds.slice(0, 4).map((cd) => <span key={cd} className="ck-tok-cond" title={cd}>{condIcon(cd)}</span>)}</div>}
                {acts.length > 0 && <div className="ck-tok-acts">{acts.map((k) => <span key={k} className="ck-tok-act" title={k}>{ACT_ICON[k]}</span>)}</div>}
                {active && c.id === active.id && !down && <div className="ck-tok-econ">{ECON.map((a) => <span key={a.k} className={"ck-tok-econd" + ((c.econ && c.econ[a.k]) ? " used" : "")} title={a.title + (c.econ && c.econ[a.k] ? " — used" : " — available")}>{a.label}</span>)}</div>}
                {down && <span className="ck-tok-down">{c.deaths && c.deaths.f >= 3 ? "💀" : "🩸"}</span>}
              </div>
            );
          })}
        </div>
        {ruler && <div className="ck-ruler-lbl" style={{ left: vp.x + ruler.x2 * vp.zoom + 8, top: vp.y + ruler.y2 * vp.zoom + 8 }}>{ruler.ft} ft</div>}
        {chip && <div className="ck-chip-ghost" style={{ left: chip.x + "px", top: chip.y + "px" }}>{chip.ability.name}</div>}
        {resTarget && (
          <ResolvePopover key={resTarget.id + resolve.ability.name} ability={resolve.ability} target={resTarget}
            source={sorted.find((c) => c.id === resolve.sourceId) || null} rollMode={st.roll}
            onDamage={onDamage} onHeal={onHeal} onPatch={onPatch} onLog={onLog} onClose={() => setResolve(null)} posStyle={posStyle} />
        )}
        {abilTip && <AbilityTip ability={abilTip.ability} posStyle={tipStyle} onClose={() => setAbilTip(null)} />}
      </div>
      <BoardDetail sel={sel} onClose={() => up({ selId: null })} onPatch={onPatch} onDamage={onDamage} onHeal={onHeal} onRemove={onRemove}
        startChip={startChip} moveChip={moveChip} endChip={endChip} />
    </div>
  );
}

function BoardDetail({ sel, onClose, onPatch, onDamage, onHeal, onRemove, startChip, moveChip, endChip }) {
  const [amt, setAmt] = useState("");
  if (!sel) return null;   // overlay panel: hidden until a token is selected, so the map stays reachable
  const pc = sel.side === "pc" ? PARTY_BY_SLUG[sel.slug] : null;
  const abilities = abilitiesFor(sel);
  const apply = (sign) => { const n = parseInt(amt, 10); if (isNaN(n) || !n) return; sign > 0 ? onHeal(sel.id, n) : onDamage(sel.id, n); setAmt(""); };
  /* action economy + spell slots (DM-overridable). `seed` = full counts from the data; sel.slots = remaining. */
  const econ = sel.econ || emptyEcon();
  const seed = slotsFor(sel);
  const slotLevels = Object.keys(seed.slots).map(Number).sort((a, b) => a - b);
  const innateNames = Object.keys(seed.innate);
  const caster = slotLevels.length > 0 || innateNames.length > 0;
  const toggleE = (k) => onPatch(sel.id, (cc) => ({ econ: { action: false, bonus: false, reaction: false, ...(cc.econ || {}), [k]: !(cc.econ && cc.econ[k]) } }));
  const setSlot = (lv, rem) => onPatch(sel.id, (cc) => ({ slots: { ...(cc.slots || {}), [lv]: rem } }));
  const setInn = (nm, rem) => onPatch(sel.id, (cc) => ({ innate: { ...(cc.innate || {}), [nm]: rem } }));
  const doRest = () => onPatch(sel.id, (cc) => { const sd = slotsFor(cc); return { slots: sd.slots, innate: sd.innate, econ: emptyEcon() }; });
  const pips = (rem, max, onSet) => (
    <span className="ck-slotpips">{Array.from({ length: max }, (_, i) => (
      <button key={i} className={"ck-pip" + (i < rem ? " on" : "")} title={i < rem ? "spend" : "restore"} onClick={() => onSet(i < rem ? i : i + 1)} />
    ))}</span>
  );
  const chip = (a, i) => (
    <button key={i} className={"ck-chip ck-chip-" + a.kind} title="drag onto a target"
      onPointerDown={(e) => startChip(e, a)} onPointerMove={moveChip} onPointerUp={endChip}>
      <span className="ck-chip-name">{a.name}</span>
      <small>{(a.level ? a.level + " · " : "") + (a.kind === "attack" ? ("+" + (a.tohit || 0) + (a.dmgExpr ? " · " + a.dmgExpr : "")) : a.kind === "save" ? ("DC " + (a.save && a.save.dc != null ? a.save.dc : "?") + (a.save && a.save.ability ? " " + a.save.ability : "")) : a.kind === "heal" ? (a.healExpr || "heal") : "effect")}</small>
    </button>
  );
  return (
    <div className="ck-detail open">
      <div className="ck-detail-head">
        <b className="ck-detail-name">{sel.name}</b>
        <span className="ck-detail-ac">{"🛡" + sel.ac}</span>
        <button className="ck-detail-rm" title="remove from fight" onClick={() => onRemove(sel.id)}>🗑</button>
        <button className="ck-detail-close" title="close — back to the map" onClick={onClose}>✕</button>
      </div>
      <div className="ck-detail-hp">
        <span className="ck-detail-hpn">{sel.hp + "/" + sel.maxhp + " HP"}</span>
        <input className="ck-amt" inputMode="numeric" placeholder="#" value={amt}
          onChange={(e) => setAmt(e.target.value.replace(/[^0-9]/g, ""))} onKeyDown={(e) => { if (e.key === "Enter") apply(-1); }} />
        <button className="ck-dmg" onClick={() => apply(-1)} title="damage">−</button>
        <button className="ck-heal" onClick={() => apply(1)} title="heal">+</button>
      </div>
      <div className="ck-detail-sec">
        <h6>Move {"·"} <span className="ck-move-ft">{speedOf(sel)} ft{sel.actions && sel.actions.dash ? " · Dash " + speedOf(sel) * 2 + " ft" : ""}</span></h6>
        <div className="ck-actbar">
          {ACTIONS.map((a) => (
            <button key={a.k} className={"ck-actbtn" + (sel.actions && sel.actions[a.k] ? " on" : "")}
              onClick={() => onPatch(sel.id, (cc) => ({ actions: { ...(cc.actions || {}), [a.k]: !(cc.actions && cc.actions[a.k]) } }))}>
              {a.icon} {a.label}
            </button>
          ))}
        </div>
      </div>
      <div className="ck-detail-sec ck-econ-sec">
        <h6>Action economy <button className="ck-rest" title="restore action economy + all spell slots (short/long rest)" onClick={doRest}>↺ rest</button></h6>
        <div className="ck-econ">
          {ECON.map((a) => (
            <button key={a.k} className={"ck-econbtn" + (econ[a.k] ? " used" : "")}
              title={a.title + (econ[a.k] ? " — used (click to restore)" : " — available (click to mark used)")} onClick={() => toggleE(a.k)}>{a.label}</button>
          ))}
        </div>
        {caster && (
          <div className="ck-slots">
            {slotLevels.map((lv) => (
              <div key={"L" + lv} className="ck-slotrow">
                <span className="ck-slotlbl">L{lv}</span>
                {pips((sel.slots && sel.slots[lv]) || 0, seed.slots[lv], (r) => setSlot(lv, r))}
                <span className="ck-slotnum">{((sel.slots && sel.slots[lv]) || 0) + "/" + seed.slots[lv]}</span>
              </div>
            ))}
            {innateNames.map((nm) => (
              <div key={nm} className="ck-slotrow">
                <span className="ck-slotlbl">{titleCase(nm)}</span>
                {pips((sel.innate && sel.innate[nm]) || 0, seed.innate[nm], (r) => setInn(nm, r))}
                <span className="ck-slotnum">{((sel.innate && sel.innate[nm]) || 0) + "/" + seed.innate[nm]}</span>
              </div>
            ))}
          </div>
        )}
      </div>
      {abilities.length > 0 && (
        <div className="ck-detail-sec">
          <h6>Drag onto a target</h6>
          <div className="ck-chips">{abilities.map(chip)}</div>
        </div>
      )}
      <div className="ck-detail-sec">
        <h6>Conditions — drag to apply</h6>
        <div className="ck-chips">
          {CONDITIONS.map((cd, i) => (
            <button key={i} className="ck-chip ck-chip-cond" title="drag onto a target"
              onPointerDown={(e) => startChip(e, { kind: "condition", cond: cd, name: cd })} onPointerMove={moveChip} onPointerUp={endChip}>
              <span className="ck-chip-name">{condIcon(cd) + " " + cd}</span>
            </button>
          ))}
        </div>
      </div>
      {pc ? <PCDetail pc={pc} />
        : sel.side === "mon" ? <div className="ck-scene ck-detail-sb" dangerouslySetInnerHTML={{ __html: E.renderSB(sel.mkey, { open: true }) }} /> : null}
    </div>
  );
}

