function InitRow({ c, active, fighting, rollMode, idx, total, onSetTurn, onReorder, onDamage, onHeal, onRemove, onPatch }) {
  const [amt, setAmt] = useState("");
  const [open, setOpen] = useState(false);
  const [dval, setDval] = useState("");
  const [showAbil, setShowAbil] = useState(false);
  const down = c.hp <= 0;
  const pc = c.side === "pc" ? PARTY_BY_SLUG[c.slug] : null;
  const pct = clamp((c.hp / c.maxhp) * 100, 0, 100);
  const band = c.hp <= 0 ? "crit" : pct <= 25 ? "crit" : pct <= 50 ? "low" : "ok";
  const apply = (sign) => {
    const n = parseInt(amt, 10); if (isNaN(n) || n === 0) return;
    sign > 0 ? onHeal(c.id, n) : onDamage(c.id, n);
    setAmt("");
  };
  const applyDeath = (d) => {
    if (isNaN(d) || d < 1 || d > 20) return;
    if (d === 20) { onHeal(c.id, 1); return; }
    onPatch(c.id, (cc) => {
      const ds = { ...cc.deaths };
      if (d === 1) ds.f = Math.min(3, ds.f + 2);
      else if (d >= 10) ds.s = Math.min(3, ds.s + 1);
      else ds.f = Math.min(3, ds.f + 1);
      return { deaths: ds };
    });
  };
  return (
    <div className={"ck-row " + c.side + (active ? " active" : "") + (down ? " down" : "")}>
      <div className="ck-rmain">
        <input className="ck-initin" title="initiative — editable" inputMode="numeric" value={c.init}
          onChange={(e) => onPatch(c.id, { init: parseInt(e.target.value, 10) || 0 })} />
        <button className="ck-name" title="make this the current turn" onClick={() => onSetTurn && onSetTurn(c.id)}>
          <span className="ck-marker">{active ? "▶" : ""}</span>{c.name}</button>
        <span className="ck-ac" title="armor class">🛡{c.ac}</span>
        <span className="ck-reorder">
          <button className="ck-seqbtn" title="move earlier in initiative" disabled={idx === 0} onClick={() => onReorder && onReorder(c.id, -1)}>↑</button>
          <button className="ck-seqbtn" title="move later in initiative" disabled={idx === total - 1} onClick={() => onReorder && onReorder(c.id, 1)}>↓</button>
        </span>
        <button className="ck-x" onClick={() => onRemove(c.id)} title="remove">✕</button>
      </div>
      <div className="ck-hpwrap">
        <div className="ck-hpbar"><i className={band} style={{ width: pct + "%" }}></i></div>
        <span className="ck-hpnum">{c.hp}<span>/{c.maxhp}</span></span>
        <input className="ck-amt" inputMode="numeric" placeholder="#" value={amt}
          onChange={(e) => setAmt(e.target.value.replace(/[^0-9]/g, ""))}
          onKeyDown={(e) => { if (e.key === "Enter") apply(-1); }} />
        <button className="ck-dmg" onClick={() => apply(-1)} title="damage">−</button>
        <button className="ck-heal" onClick={() => apply(1)} title="heal">+</button>
      </div>
      {down && c.side === "pc" && (
        <div className="ck-death">
          <span className="ck-dlbl">Death saves</span>
          <span className="pips s">{[0, 1, 2].map((i) => <i key={i} className={i < c.deaths.s ? "on" : ""}></i>)}</span>
          <span className="pips f">{[0, 1, 2].map((i) => <i key={i} className={i < c.deaths.f ? "on" : ""}></i>)}</span>
          {rollMode === "me" ? (
            <span className="ck-dmanual">
              <input className="ck-d20in sm" inputMode="numeric" placeholder="d20" value={dval}
                onChange={(e) => setDval(e.target.value.replace(/[^0-9]/g, ""))}
                onKeyDown={(e) => { if (e.key === "Enter") { applyDeath(parseInt(dval, 10)); setDval(""); } }} />
              <button className="ck-droll" onClick={() => { applyDeath(parseInt(dval, 10)); setDval(""); }}>ok</button>
            </span>
          ) : (
            <button className="ck-droll" onClick={() => applyDeath(E.rollDie(20))}>roll d20</button>
          )}
          {c.deaths.f >= 3 && <span className="ck-dead">DEAD</span>}
          {c.deaths.s >= 3 && <span className="ck-stable">STABLE</span>}
        </div>
      )}
      {pc && (
        <div className="ck-pcabil">
          <button className="ck-abilbtn" onClick={() => setShowAbil((o) => !o)}>
            {showAbil ? "▾ abilities & spells" : "▸ abilities & spells"}
          </button>
          {showAbil && <PCDetail pc={pc} />}
        </div>
      )}
      <div className="ck-conds">
        {c.conds.map((cd) => (
          <button key={cd} className="ck-cond" onClick={() => onPatch(c.id, (cc) => ({ conds: cc.conds.filter((x) => x !== cd) }))}>{cd} ✕</button>
        ))}
        <button className="ck-condadd" onClick={() => setOpen((o) => !o)}>+ condition</button>
        {open && (
          <div className="ck-condmenu" onMouseLeave={() => setOpen(false)}>
            {CONDITIONS.filter((cd) => !c.conds.includes(cd)).map((cd) => (
              <button key={cd} onClick={() => { onPatch(c.id, (cc) => ({ conds: [...cc.conds, cd] })); setOpen(false); }}>{cd}</button>
            ))}
          </div>
        )}
      </div>
    </div>
  );
}

