/* initiative sort: a manual `seq` override (baked by drag/↑↓ reorder) wins when present,
   so the DM's order sticks and nextTurn follows the displayed list; otherwise fall back to
   init desc → dex mod → name. Sparse seq is fine — un-seq'd combatants sink below seq'd ones. */
function sortInit(list) {
  return [...list].sort((a, b) => {
    const aS = typeof a.seq === "number", bS = typeof b.seq === "number";
    if (aS && bS) return a.seq - b.seq;
    if (aS !== bS) return aS ? -1 : 1;
    return (b.init - a.init) || (b.dexmod - a.dexmod) || a.name.localeCompare(b.name);
  });
}

/* ---- board placement: PCs along the bottom band, monsters along the top, spread out.
   Positions are fractions [0..1] of the board so they survive reload/resize. ---- */
function placeDefault(side, i) {
  const col = i % 6, row = Math.floor(i / 6);
  const x = 0.1 + col * 0.15 + (row % 2) * 0.06;
  const y = side === "pc" ? 0.84 - row * 0.13 : 0.16 + row * 0.13;
  return { x: clamp(x, 0.05, 0.95), y: clamp(y, 0.05, 0.95) };
}
/* normalize each combatant: back-fill x,y (old saves + fresh preloads/adds) AND the v2.2
   action/slot economy (econ/slots/innate). Idempotent — never clobbers existing values, so a
   running fight's spent slots/positions survive a reload. `seq` stays sparse (set only on reorder). */
function withPositions(list) {
  const idx = { pc: 0, mon: 0 };
  return (list || []).map((c) => {
    const side = c.side === "pc" ? "pc" : "mon";
    const i = idx[side]++;
    const out = (typeof c.x === "number" && typeof c.y === "number") ? { ...c } : { ...c, ...placeDefault(side, i) };
    if (!out.econ) out.econ = emptyEcon();
    if (!out.slots || !out.innate) { const sd = slotsFor(out); if (!out.slots) out.slots = sd.slots; if (!out.innate) out.innate = sd.innate; }
    return out;
  });
}

